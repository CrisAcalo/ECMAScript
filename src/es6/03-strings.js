//Concatenaciones

let hello = 'Hello';
let world = 'world';

//(a.ES6)
let phrase = hello+' '+world+'!';
console.log(phrase);

//(d.ES6) TEemplate Literals
let phrase2 = `${hello} ${world}!`;
console.log(phrase2);

//Multi-line strings
let lorem = 'String de prueba.\nY acabo de hacer un salto de línea';
console.log(lorem);

//Template Literals
let lorem2 = 
`
String de prueba 2.
Y acá acabo de hacer un salto de línea en el mismo archivo ;-;
`;
console.log(lorem2);